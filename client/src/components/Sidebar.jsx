const Sidebar = ({ currentView, onNavigate, onLogout, userEmail }) => {
  // Extract user initials for avatar
  const getInitials = (email) => {
    if (!email) return "U";
    return email.charAt(0).toUpperCase();
  };

  // Navigation items configuration
  const navItems = [
    { id: "dashboard", label: "Dashboard", icon: Icons.Home },
    { id: "add", label: "Add Job", icon: Icons.PlusCircle },
    { id: "calendar", label: "Calendar", icon: Icons.Calendar },
    { id: "stats", label: "Statistics", icon: Icons.BarChart },
    { id: "settings", label: "Settings", icon: Icons.Settings },
  ];

  return (
    <aside className="sidebar">
      {/* Logo/Header */}
      <div className="sidebar-header">
        <div className="sidebar-logo">
          <Icons.Briefcase />
          <span>Job Tracker</span>
        </div>
      </div>

      {/* Navigation */}
      <nav className="sidebar-nav">
        <div className="nav-section">
          <span className="nav-section-title">Menu</span>
          {navItems.map((item) => (
            <button
              key={item.id}
              className={`nav-item ${currentView === item.id ? "active" : ""}`}
              onClick={() => onNavigate(item.id)}
            >
              <item.icon />
              <span>{item.label}</span>
            </button>
          ))}
        </div>
      </nav>

      {/* User Menu / Footer */}
      <div className="sidebar-footer">
        <div className="user-menu">
          <div className="user-avatar">{getInitials(userEmail)}</div>
          <div className="user-info">
            <div className="user-name">My Account</div>
            <div className="user-email-text">{userEmail}</div>
          </div>
        </div>
        <button
          className="nav-item"
          onClick={onLogout}
          style={{ marginTop: "0.5rem" }}
        >
          <Icons.LogOut />
          <span>Logout</span>
        </button>
      </div>
    </aside>
  );
};

export default Sidebar;
